package com.tdarray.programs;

public class Character {

	public static void main(String[] args) {
		char wfh[][]={{'a','b','c'},{'d','e','f'},{'x','y','z'}};
		for(int i =0;i<=0;i++)
		{
			for(int j=0;j<wfh[i].length;j++)
			{
				System.out.print(wfh[i][j]+" ");

			}
			System.out.println();
		}
	}

}
